//PROMPT:
//In JavaScript, prompt() is a built-in function that displays a dialog box asking the user to enter some text. 
// It is commonly used to collect simple input from the user.

//Instances of Prompt:
//Example 1: Basic Prompt

let name = prompt("What is your name?");

console.log("Hello, " + name + "!");
 
//If the user enters Jesse, the output will be:
//Hello, Jesse!
